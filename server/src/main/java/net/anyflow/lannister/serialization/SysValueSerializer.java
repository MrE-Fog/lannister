package net.anyflow.lannister.serialization;

import java.io.IOException;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.SerializerProvider;

import net.anyflow.lannister.Statistics.SysValue;

public final class SysValueSerializer extends com.fasterxml.jackson.databind.JsonSerializer<SysValue> {
	@Override
	public void serialize(SysValue value, JsonGenerator generator, SerializerProvider provider)
			throws IOException, JsonProcessingException {

		generator.writeString(value.value());
	}
}